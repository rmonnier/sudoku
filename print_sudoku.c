#include <unistd.h>

void	print_sudoku(char **argv)
{
	int i;
	int j;

	i = 0;
	while (i < 9)
	{
		j = 0;
		while (j < 9)
		{
			write(1, (argv[i] + j), 1);
			if (j < 8)
				write(1, " ", 1);
			else
				write(1, "\n", 1);
			j++;
		}
		i++;
	}
	return ;
}
